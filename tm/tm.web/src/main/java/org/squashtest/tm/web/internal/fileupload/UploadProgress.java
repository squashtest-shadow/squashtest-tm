package org.squashtest.tm.web.internal.fileupload;

public class UploadProgress {
	
    /** The total number of kilobytes, which are being read. */
	private long totalSize = 0;
	
    /** The total number of kilobytes, which have been read so far. */
	private long bytesRead = 0;
	
    /** The percentage of kilobytes, which have been read so far. */
	private int percentage = 0;

	public int getPercentage() {
		return percentage;
	}

	public void setPercentage(int percentage) {
		this.percentage = percentage;
	}

	public long getTotalSize() {
		return totalSize;
	}

	public void setTotalSize(long totalSize) {
		this.totalSize = totalSize;
	}

	public long getBytesRead() {
		return bytesRead;
	}

	public void setBytesRead(long bytesRead) {
		this.bytesRead = bytesRead;
	}
}